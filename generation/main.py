



class Animal:
    def __init__(self):
        hunger = 0
        thirst = 0
        age = 0

    def eat(hunger):
        hunger = hunger - 10
        return hunger

    def drink(self, thirst):
        thirst = thirst - 10
        return thirst

class Option:
    def alerts(self, hunger, thirst, age):
        if hunger >= 50:
            print("I need to eat!")
        if thirst >= 50:
            print("I need a drink!")
        if age >= 10:
            print("I'm getting old!")

pet = Animal
